void gfx_draw_text(int x, int y, const char *text, color_t c, int size) {
    if (!text) return;
    int ox = x;
    while (*text) {
        unsigned char ch = (unsigned char)*text++;
        if (ch == '\n') { x = ox; y += 9 * size; continue; }
        if (ch > 127) ch = '?';
        for (int row = 0; row < 8; row++) {
            uint8_t bits = s_font8x8[ch][row];
            for (int col = 0; col < 8; col++) {
                if (bits & (0x80 >> col)) {
                    for (int sy = 0; sy < size; sy++)
                        for (int sx = 0; sx < size; sx++)
                            set_pixel(x + col * size + sx, y + row * size + sy, c);
                }
            }
        }
        x += 8 * size + size;
    }
}

void gfx_draw_progress_bar(int x, int y, int w, int h, float pct, color_t bg, color_t fg) {
    if (pct < 0.0f) pct = 0.0f;
    if (pct > 1.0f) pct = 1.0f;
    /* Border */
    gfx_draw_rect(x, y, w, h, COLOR_GRAY);
    /* Background */
    gfx_draw_rect(x + 2, y + 2, w - 4, h - 4, bg);
    /* Fill */
    int fill = (int)((w - 4) * pct);
    if (fill > 0)
        gfx_draw_rect(x + 2, y + 2, fill, h - 4, fg);
}

void gfx_draw_gradient_bg(color_t top, color_t bottom) {
    for (int y = 0; y < SCREEN_HEIGHT; y++) {
        float t = (float)y / (float)(SCREEN_HEIGHT - 1);
        color_t c = {
            (uint8_t)(top.r + (int)((bottom.r - top.r) * t)),
            (uint8_t)(top.g + (int)((bottom.g - top.g) * t)),
            (uint8_t)(top.b + (int)((bottom.b - top.b) * t)),
            255
        };
        for (int x = 0; x < SCREEN_WIDTH; x++)
            set_pixel(x, y, c);
    }
}

void gfx_draw_image(int x, int y, int w, int h, const void *rgba_data) {
    if (!rgba_data) return;
    const uint8_t *src = (const uint8_t *)rgba_data;
    for (int dy = 0; dy < h; dy++) {
        for (int dx = 0; dx < w; dx++) {
            color_t c;
            c.r = src[(dy * w + dx) * 4 + 0];
            c.g = src[(dy * w + dx) * 4 + 1];
            c.b = src[(dy * w + dx) * 4 + 2];
            c.a = src[(dy * w + dx) * 4 + 3];
            set_pixel(x + dx, y + dy, c);
        }
    }
}

void gfx_animate_frame(void) {
    s_anim_tick++;
}
